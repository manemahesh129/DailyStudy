let a=11, b=3
console.log(a);
console.log(b);

// Arithmetic Operators
let sum = a + b; // Addition
let difference = a - b; // Subtraction
let product = a * b; // Multiplication
let quotient = a / b; // Division
let remainder = a % b; // Modulus // Remainder of the division of a by b
let exponentiation = a ** b; // Exponentiation   // a raised to the power of b
console.log("Sum is:", sum);
console.log("Difference is:", difference);
console.log("Product is:", product);
console.log("Quotient is:", quotient);
console.log("Remainder is:", remainder);
console.log("Exponentiation is:", exponentiation);

