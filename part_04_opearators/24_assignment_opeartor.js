// Assignment Operators
// Assignment operators are used to assign values to variables.
// The most common assignment operator is the equals sign (=).
let x =10; // Assigns the value 10 to the variable x
console.log(x); // Output: 10
x=x+12; // Assigns the result of x + 12 back to x
console.log(x); // Output: 22

// Compound Assignment Operators
// These operators combine an arithmetic operation with assignment.
x += 5; // Equivalent to x = x + 5
console.log(x); // Output: 15

x -= 3; // Equivalent to x = x - 3
console.log(x);

x *= 2; // Equivalent to x = x * 2
console.log(x);

x /= 4; // Equivalent to x = x / 4
console.log(x);

x %= 3; // Equivalent to x = x % 3
console.log(x);