// Comparison Operators
// Comparison operators are used to compare two values and return a boolean value (true or false).
let a = 10;
let b = 5;
console.log(a > b); // Output: true (a is greater than b)
console.log(a < b); // Output: false (a is not less than b)
console.log(a >= b); // Output: true (a is greater than or equal to b)
console.log(a <= b); // Output: false (a is not less than or equal to b)
console.log(a == b); // Output: false (a is not equal to b)
console.log(a != b); // Output: true (a is not equal to b)

//// Rule breaker - need to remember
console.log(5!='5'); // Output: false (5 is equal to '5' after type coercion)
console.log(5!=='5'); // Output: true (5 is not equal to '5' without type coercion)