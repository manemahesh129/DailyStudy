// String Operators
// The + operator can be used to concatenate (combine) strings in JavaScript.
let str1 = "Hello, ";
let str2 = "world!";
let result = str1 + str2; // Concatenates str1 and str2
console.log(result); // Output: "Hello, world!"

// You can also use the + operator to concatenate string literals directly.
let greeting = "Hello, " + "world!";
console.log(greeting); // Output: "Hello, world!"

let name ="Mahesh";
let last_name ="Mane";
let full_name = name + " " + last_name;
console.log(full_name);