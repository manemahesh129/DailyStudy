// Ternary Operator
// The ternary operator is a concise way to perform conditional operations. It takes three operands: a condition, an expression to execute if the condition is true, and an expression to execute if the condition is false. The syntax is: condition ? expressionIfTrue : expressionIfFalse.

let age = 18;
let canVote = age >= 18 ? "Yes, you can vote." : "No, you cannot vote.";
console.log(canVote); // Output: Yes, you can vote.

// Example with a different condition
let temperature = 30;
let weather = temperature > 25 ? "It's hot outside." : "It's cold outside.";
console.log(weather); // Output: It's hot outside.

